# While loops
i = 1
while i <= 5:
    print(i)
    i = i + 1
# Lists use []
names = ["Key", "John"," Bob", "Joe"]
print(names)

# For loops
numbers = [1, 2, 3, 4, 5]
for item in numbers:
    print(item)

# Range function
numbers = range(5, 12, 3)
for number in numbers:
    print(number)
    
# Tuples use ()
numbers = (1, 2, 3, 4)

